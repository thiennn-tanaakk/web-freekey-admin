/** 入力フォーマット */
export namespace INPUT_FORMAT {
  /** パスワード
   * ・英大文字、英小文字、数字、記号（= + - ^ $ * . [ ] { } ( ) ? " ! @ # % & / \ , > < ' : ; | _ ~ `）を許可
   * ・文字数は８文字以上
   * ・英大文字、英小文字、数字はそれぞれ１文字以上含めなければならない
   */
  export const PASSWORD =
    /^((?=.*[a-z])(?=.*[A-Z])(?=.*[0-9]))[a-zA-Z0-9=+-^$*.¥[\]{}()?"!@#%&/¥,><':;|_~`]{8,}$/;

  export const TWO_BYTE_NUMBER = /[０１２３４５６７８９]+/;
}
